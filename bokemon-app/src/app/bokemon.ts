
export class Bokemon {

  id: number = 0;
  name: string;
  hp: number;
  atk: number;
  def: number;
  spd: number;
  lvl: number;
  exp: number;

  constructor(id: number, name: string, hp: number, atk: number, def: number, spd: number, lvl: number, exp: number){
    this.id = id;
    this.name = name;
    this.hp = hp;
    this.atk = atk;
    this.def = def;
    this.spd = spd;
    this.lvl = lvl;
    this.exp = exp;
  }





}
